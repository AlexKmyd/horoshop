<?php


namespace App\Core\FacadeBundle\v1\Requests;


class GetUserRequest extends AbstractRequest
{
    /**
     * @var int
     */
    protected $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }
}